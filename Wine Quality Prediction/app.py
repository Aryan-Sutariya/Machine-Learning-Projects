# Prediction
# ─────────────────────────────────────────────────────────────────────────────
def build_input_df(user_vals):
    row = dict(user_vals)
    # Derived features (same as training)
    row["acidity_ratio"]   = row["fixed acidity"]  / (row["volatile acidity"] + 1e-9)
    row["so2_ratio"]       = row["free sulfur dioxide"] / (row["total sulfur dioxide"] + 1e-9)
    row["alcohol_density"] = row["alcohol"] / row["density"]
    return pd.DataFrame([row])[feature_names]

def quality_emoji(score):
    if   score >= 7.5: return "🏆 Excellent"
    elif score >= 6.5: return "😊 Good"
    elif score >= 5.5: return "🙂 Average"
    else:              return "😕 Below Average"

def quality_color(score):
    if   score >= 7.5: return "#1a7a1a"
    elif score >= 6.5: return "#2171b5"
    elif score >= 5.5: return "#d95f02"
    else:              return "#a50026"

# Compute prediction immediately (live) and on button press
input_df   = build_input_df(user_vals)
input_sc   = scaler.transform(input_df)
pred_score = round(float(model.predict(input_sc)[0]), 2)

