@st.cache_resource
def load_artifacts():
    missing = [f for f in ["best_wine_model.pkl", "wine_scaler.pkl",
                            "feature_names.pkl", "model_meta.pkl"]
               if not os.path.exists(f)]
    if missing:
        return None, None, None, None
    model    = joblib.load("best_wine_model.pkl")
    scaler   = joblib.load("wine_scaler.pkl")
    features = joblib.load("feature_names.pkl")
    meta     = joblib.load("model_meta.pkl")
    return model, scaler, features, meta

@st.cache_data
def load_data():
    if not os.path.exists("winequality-red.csv"):
        return None
    df = pd.read_csv("winequality-red.csv")
    df["acidity_ratio"]   = df["fixed acidity"] / (df["volatile acidity"] + 1e-9)
    df["so2_ratio"]       = df["free sulfur dioxide"] / (df["total sulfur dioxide"] + 1e-9)
    df["alcohol_density"] = df["alcohol"] / df["density"]
    return df

model, scaler, feature_names, meta = load_artifacts()
df = load_data()


